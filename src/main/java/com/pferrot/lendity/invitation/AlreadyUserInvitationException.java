package com.pferrot.lendity.invitation;

public class AlreadyUserInvitationException extends InvitationException {

	public AlreadyUserInvitationException() {
		super();
	}

	public AlreadyUserInvitationException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public AlreadyUserInvitationException(String arg0) {
		super(arg0);
	}

	public AlreadyUserInvitationException(Throwable arg0) {
		super(arg0);
	}
}
