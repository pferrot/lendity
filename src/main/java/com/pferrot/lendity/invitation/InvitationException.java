package com.pferrot.lendity.invitation;

public class InvitationException extends Exception {

	public InvitationException() {
		super();
	}

	public InvitationException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public InvitationException(String arg0) {
		super(arg0);
	}

	public InvitationException(Throwable arg0) {
		super(arg0);
	}
}
